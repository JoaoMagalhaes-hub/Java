package Aula_5;

public class Pessoa {
    
    private double cep, bairro, rua, idade, nome;

    public double getCep() {
        return this.cep;
    }

    public void setCep(double cep) {
        this.cep = cep;
    }

    public double getBairro() {
        return this.bairro;
    }

    public void setBairro(double bairro) {
        this.bairro = bairro;
    }

    public double getRua() {
        return this.rua;
    }

    public void setRua(double rua) {
        this.rua = rua;
    }

    public double getIdade() {
        return this.idade;
    }

    public void setIdade(double idade) {
        this.idade = idade;
    }

    public double getNome() {
        return this.nome;
    }

    public void setNome(double nome) {
        this.nome = nome;
    }
}
